package edu.pict.ecommerce.authservice.model.enums;

/**
 * Enum representing the roles that a user can have within the system.
 */
public enum Role {

    /**
     * Represents an administrator user with elevated privileges.
     */
    ADMIN,

    /**
     * Represents a student user.
     */
    STUDENT,

    /**
     * Represents a teacher user.
     */
    TEACHER
}
